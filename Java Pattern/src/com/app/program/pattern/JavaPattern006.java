/**
 * Java Pattern
 * 
 * @author Kalidas A Daundkar
 *
 */
public class JavaPattern006 {
	public static void main(String[] args) {
		for (int i = 5; i > 0; i--) {
			for (int j = 0; j < 6; j++) {
				System.out.print(i);
			}
			System.out.println();
		}
	}

}
