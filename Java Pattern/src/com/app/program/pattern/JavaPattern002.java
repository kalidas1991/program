package com.app.program.pattern;

/**
 * Java Pattern
 * 
 * @author Kalidas A Daundkar
 *
 */
public class JavaPattern002 {
	public static void main(String[] args) {

		for (int i = 1; i < 6; i++) {
			for (int j = 0; j < 5; j++) {
				System.out.print(i);
			}
			System.out.println();
		}
	}

}
